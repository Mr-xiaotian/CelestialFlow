/**
 * 切换全局语言设置
 * @param {Lang} lang - 目标语言标识
 */
function setLang(lang: Lang) {
  currentLang = lang;
  document.documentElement.lang = currentLang;
}

/**
 * 根据 key 获取翻译文本，支持简单的占位符替换
 * @param {string} key - 翻译键名
 * @param {...string} args - 用于替换 {0}, {1}... 的参数列表
 * @returns {string} 翻译后的文本
 */
function t(key: string, ...args: string[]): string {
  let s =
    translations[currentLang]?.[key] ?? translations["zh-CN"]?.[key] ?? key;
  for (let i = 0; i < args.length; i++) {
    s = s.replace(`{${i}}`, args[i]);
  }
  return s;
}

/**
 * 扫描全页带有特定 data-i18n 属性的元素并应用翻译
 * - data-i18n: 替换 textContent
 * - data-i18n-placeholder: 替换 placeholder
 * - data-i18n-title: 替换 title 属性
 * - data-i18n-aria-label: 替换 aria-label 属性
 */
function applyI18nDOM() {
  document.querySelectorAll<HTMLElement>("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n")!;
    el.textContent = t(key);
  });
  document
    .querySelectorAll<HTMLElement>("[data-i18n-placeholder]")
    .forEach((el) => {
      (el as HTMLInputElement).placeholder = t(
        el.getAttribute("data-i18n-placeholder")!,
      );
    });
  document.querySelectorAll<HTMLElement>("[data-i18n-title]").forEach((el) => {
    el.title = t(el.getAttribute("data-i18n-title")!);
  });
  document
    .querySelectorAll<HTMLElement>("[data-i18n-aria-label]")
    .forEach((el) => {
      el.setAttribute(
        "aria-label",
        t(el.getAttribute("data-i18n-aria-label")!),
      );
    });
  document.title = t("app.title");
}
